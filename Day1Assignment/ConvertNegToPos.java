package Day1Assignment;

public class ConvertNegToPos { 
	public static void main(String[] args) {
		int input = -40;
		int output;
		if (input <0) {
			output = input * -1;
			System.out.println("The number "+input+" converted to "+output);
		}
		else {
			System.out.println("The given number is positive number");
		}
			
		
		
	}

}
